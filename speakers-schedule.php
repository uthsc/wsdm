            <h4>Emergency Medicine: Back to Basics</h4>

            <p>
                <strong>8:30 a.m. - 4:45 p.m.<br/> Course Code: 004AM / 6 hours CE<br/> Recommended for the Entire Dental Team</strong>
            </p>

            <p>Life-threatening emergencies can occur anytime, anywhere and to anyone. Such situations are more likely to occur in the dental office due to the increased level of stress that is so often present.</p>

            <p>Dr. Malamed will discuss those areas considered vital to a proper understanding of emergency medicine: prevention, preparation, and recognition and management.</p>

            <p>The morning session includes a discussion of preparation involving the office and staff as well as a review of the office emergency kit (drugs and equipment). The afternoon session discusses recognition and management of specific emergency situations and includes the management of unconsciousness, respiratory distress, seizures, drug-related emergencies and chest pain, including cardiac arrest and use of the AED.</p>
